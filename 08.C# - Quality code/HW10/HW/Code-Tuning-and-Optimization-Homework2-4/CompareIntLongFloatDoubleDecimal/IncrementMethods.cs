using System;

public class IncrementMethods
{
    public static int IncrementInt(int value)
    {
        return value++;
    }

    public static long IncrementLong(long value)
    {
        return value++;
    }

    public static float IncrementFloat(float value)
    {
        return value++;
    }

    public static double IncrementDouble(double value)
    {
        return value++;
    }

    public static decimal IncrementDecimal(decimal value)
    {
        return value++;
    }
}