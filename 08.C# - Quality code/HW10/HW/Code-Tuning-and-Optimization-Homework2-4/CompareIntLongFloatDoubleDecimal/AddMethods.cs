using System;

public class AddMethods
{
    public static int AddInt(int first, int second)
    {
        return first + second;
    }

    public static long AddLong(long first, long second)
    {
        return first + second;
    }

    public static float AddFloat(float first, float second)
    {
        return first + second;
    }

    public static double AddDouble(double first, double second)
    {
        return first + second;
    }

    public static decimal AddDecimal(decimal first, decimal second)
    {
        return first + second;
    }
}