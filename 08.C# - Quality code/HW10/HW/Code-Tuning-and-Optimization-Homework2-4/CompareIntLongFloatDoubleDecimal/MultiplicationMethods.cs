using System;

public class MultiplicationMethods
{
    public static int MultiplyInt(int first, int second)
    {
        return first * second;
    }

    public static long MultiplyLong(long first, long second)
    {
        return first * second;
    }

    public static float MultiplyFloat(float first, float second)
    {
        return first * second;
    }

    public static double MultiplyDouble(double first, double second)
    {
        return first * second;
    }

    public static decimal MultiplyDecimal(decimal first, decimal second)
    {
        return first * second;
    }
}