using System;

public class DivisionMethods
{
    public static int DivideInt(int first, int second)
    {
        return first / second;
    }

    public static long DivideLong(long first, long second)
    {
        return first / second;
    }

    public static float DivideFloat(float first, float second)
    {
        return first / second;
    }

    public static double DivideDouble(double first, double second)
    {
        return first / second;
    }

    public static decimal DivideDecimal(decimal first, decimal second)
    {
        return first / second;
    }
}