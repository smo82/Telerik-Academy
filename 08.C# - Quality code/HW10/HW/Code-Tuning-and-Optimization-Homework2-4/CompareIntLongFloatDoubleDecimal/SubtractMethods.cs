using System;

public class SubtractMethods
{
    public static int SubtractInt(int first, int second)
    {
        return first - second;
    }

    public static long SubtractLong(long first, long second)
    {
        return first - second;
    }

    public static float SubtractFloat(float first, float second)
    {
        return first - second;
    }

    public static double SubtractDouble(double first, double second)
    {
        return first - second;
    }

    public static decimal SubtractDecimal(decimal first, decimal second)
    {
        return first - second;
    }
}